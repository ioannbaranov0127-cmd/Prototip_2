"""
Простые человекочитаемые пояснения к сообщениям Python (для школьников).
"""
from __future__ import annotations


def explain_stderr(stderr: str) -> dict[str, str | list[str]]:
    """
    Возвращает заголовок для ребёнка, короткие советы и (опционально) исходный stderr.
    """
    text = (stderr or "").strip()
    if not text:
        return {
            "title": "Ошибка выполнения",
            "tips": ["Попробуйте запустить программу ещё раз."],
            "raw": "",
        }

    lower = text.lower()
    tips: list[str] = []

    if "syntaxerror" in lower:
        title = "Синтаксис: Python не смог разобрать запись кода"
        tips = [
            "Часто не хватает скобки ) или кавычки \"...\".",
            "Проверьте двоеточие : после if, for, while, def.",
            "Сравнение «равно» в условии пишется как ==, а не одним знаком =.",
        ]
    elif "indentationerror" in lower or "indenterro" in lower:
        title = "Отступы: строка начинается не так, как ожидает Python"
        tips = [
            "После if, for, while, def следующая строка должна быть с отступом (обычно 4 пробела).",
            "Внутри одного блока отступы должны быть одинаковыми.",
        ]
    elif "nameerror" in lower:
        title = "Имя не найдено: переменная или функция ещё не создана"
        tips = [
            "Проверьте, нет ли опечатки в имени.",
            "Сначала присвойте значение (например, name = \"Аня\"), потом используйте print(name).",
        ]
    elif "typeerror" in lower:
        title = "Типы данных: такие значения нельзя складывать или сравнивать так, как написано"
        tips = [
            "Нельзя складывать строку и число без преобразования: int(\"5\") или str(5).",
        ]
    elif "valueerror" in lower:
        title = "Значение не подходит: функция не смогла его обработать"
        tips = [
            "Например, int(\"привет\") не получится — нужна строка с числом.",
        ]
    elif "eof" in lower and "reading" in lower:
        title = "Не хватило ввода: программа ждала ещё данные"
        tips = [
            "Добавьте строку ввода в консоли или проверьте количество вызовов input().",
        ]
    elif "zerodivisionerror" in lower:
        title = "Деление на ноль"
        tips = ["Проверьте, не равен ли нулю знаменатель.", "Можно добавить проверку if b != 0: ..."]
    elif "keyboardinterrupt" in lower:
        title = "Выполнение прервано"
        tips = []
    else:
        title = "Программа остановилась с ошибкой"
        tips = [
            "Прочитайте последнюю строку сообщения — там часто указана причина.",
            "Проверьте код вокруг указанной строки.",
        ]

    return {"title": title, "tips": tips, "raw": text}
